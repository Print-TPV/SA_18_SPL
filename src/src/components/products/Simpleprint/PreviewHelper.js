export const removeFileExtension = (name) => {
  if (name.toLowerCase().endsWith('.pdf')) {
    return name.slice(0, -4)
  } else if (name.toLowerCase().endsWith('.doc')) {
    return name.slice(0, -4)
  } else if (name.toLowerCase().endsWith('.docx')) {
    return name.slice(0, -5)
  } else {
    return name
  }
}

