/**
 * Insert tab before page(Double sided preview)
 * @param {Array} arr - Array of page numbers
 * @param {Number} tabCount - Number of tabs added.
 * @param {{}} tabData - Details of tab added
 * @return {Array} - Return page array
 */
export const insertTabBeforeDoubleSidedPage = (arr, tabCount, tabData) => {
  if (tabCount && tabCount > 0 && arr && arr.length > 0 && arr && arr.length > 0 && tabData.before) {
    let counter = 0;
    tabData.details.forEach(tab => {
      const index = arr.indexOf(tab.page);
      arr.splice(index, 0, `Tab`);
      arr.splice(index, 0, `Tab`);
      if (counter % 2 === 0 && tab.page % 2 === 0) {
        counter = counter + 1;
        arr.splice(index, 0, `${tab.page - 1}B`);
      }
      if (counter % 2 !== 0 && tab.page % 2 !== 0) {
        counter = counter + 1;
        arr.splice(index, 0, `${tab.page - 1}B`);
      }
    })
  }
  return arr;
}

/**
 * Insert tab after page(Double sided preview)
 * @param {Array} arr - Array of page numbers
 * @param {Number} tabCount - Number of tabs added.
 * @param {{}} tabData - Details of tab added
 * @return {Array} - Return page array
 */
export const insertTabAfterDoubleSidedPage = (arr, tabCount, tabData) => {
  if (tabCount && tabCount > 0 && arr && arr.length > 0 && !tabData.before) {
    tabData.details.forEach(tab => {
      const index = arr.indexOf(tab.page);
      arr.splice(index + 1, 0, `Tab`);
      arr.splice(index + 1, 0, `Tab`);
    })
  }
  if (tabCount && tabCount > 0 && arr && arr.length > 0 && !tabData.before) {
    let counter = 0;
    tabData.details.forEach(tab => {
      const index = arr.indexOf(tab.page);
      if (counter % 2 === 0 && tab.page % 2 !== 0) {
        counter = counter + 1;
        arr.splice(index + 1, 0, `${tab.page}B`);
      }
      if (counter % 2 !== 0 && tab.page % 2 === 0) {
        counter = counter + 1;
        arr.splice(index + 1, 0, `${tab.page}B`);
      }
    })
  }
  return arr;
}

/**
 * Insert tab before/after page(Single sided preview)
 * @param {Array} arr - Array of page numbers
 * @param {Number} tabCount - Number of tabs added.
 * @param {{}} tabData - Details of tab added
 * @return {Array} - Return page array
 */
export const insertTabSingleSidedPage = (arr, tabCount, tabData) => {
  if (tabCount && tabCount > 0 && arr && arr.length > 0) {
    tabData.details.forEach(tab => {
      const index = arr.indexOf(tab.page);
      if (index !== -1 && tabData.before) {
        arr.splice(index, 0, `Tab`);
      } else {
        arr.splice(index + 1, 0, `Tab`);
      }
    })
  }
  return arr;
}


/**
 * To create pagination array for double sided desktop view
 * @param {Number} totalItems - Total number of items
 * @param {Boolean} hasDarkFrontCover - Dark front presentation cover
 * @param {Boolean} hasDarkBackCover - Dark back presentation cover
 * @param {Number} tabCount - Number of tabs added.
 * @param {{}} tabData - Details of tab added
 * @return {Array} - Return page array
 */
export const getPagesArrLeftSideView = (totalItems, hasDarkFrontCover, hasDarkBackCover, tabCount, tabData) => {
  let arr = []
  let totalItemsArr = Array.from({ length: totalItems }, (_, i) => i + 1)
  if (tabCount > 0 && tabData.before) {
    totalItemsArr = insertTabBeforeDoubleSidedPage(totalItemsArr, tabCount, tabData);
  }else {
    totalItemsArr = insertTabAfterDoubleSidedPage(totalItemsArr, tabCount, tabData);
  }
  for (let i = 1; i <= totalItemsArr.length; i++) {
    if (i === 1 || ((i === totalItemsArr.length) && totalItemsArr.length % 2 === 0)) {
      arr.push(totalItemsArr[i-1])
    } else if (i === totalItemsArr.length && totalItemsArr.length % 2 !== 0) {
      arr.push('End')
    } else if (i % 2 === 0) {
      arr.push(`${totalItemsArr[i-1]},${totalItemsArr[i]}`)
    } else {
      //do nothing
    }
  }
  if (hasDarkFrontCover) {
    arr.splice(0, 0, 'Front')
  }
  if (hasDarkBackCover && !arr.includes('End')) {
    arr.push('End')
  }
  if (arr.length > 1 && hasDarkFrontCover) {
    arr[1] = `Front,${arr[1]}`
  }
  if (totalItemsArr.length % 2 === 0 && hasDarkBackCover) {
    arr[arr.length - 2] = `${arr[arr.length - 2]},End`
  }
  return arr
}

/**
 * To create pagination array
 * @param {Number} totalItems - Total number of items
 * @param {Boolean} hasDarkFrontCover - Dark front presentation cover
 * @param {Boolean} hasBindingBackCover - Binding back cover
 * @param {Number} tabCount - Number of tabs added.
 * @param {{}} tabData - Details of tab added
 * @return {Array} - Return page array
 */
export const getPagesArrRightSideView = (totalItems, hasDarkFrontCover, hasBindingBackCover, tabCount, tabData) => {
  let arr = Array.from({length: totalItems}, (_, i) => i + 1)
  arr = insertTabSingleSidedPage(arr, tabCount, tabData);
  if (hasDarkFrontCover) {
    arr.splice(0, 0, 'Front')
  }
  if (hasBindingBackCover || totalItems % 2 != 0) {
    arr.push('End')
  }
  return arr
}

export const calculateInchesFromPixels = (size) => {
  if (size && typeof size === 'string' && size.includes("_") && size.includes("x")) {
    const _s = size.split("_");
    if (_s.length === 2) {
      const _px = _s[1].split("x");
      if (_px.length === 2) {
        const divisor = 72;
        return `${_px[0] / divisor}"x${_px[1] / divisor}"`;
      } else {
        return size;
      }
    } else {
      return size;
    }
  }
  return size;
};