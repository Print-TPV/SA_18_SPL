export const calculateAspectRatio = (_size) => {
  if(!checkStandardPageSize(_size)) {
    const _s = _size.split("_");
    const _px = _s[1].split("x");
    const gcd = (a, b) => b === 0 ? a : gcd(b, a % b);
    const divisor = gcd(_px[0], _px[1]);
    return [_px[0] / divisor, _px[1] / divisor];
  }
  return null;
}

export const checkStandardPageSize = (_size) => {
  const parts = _size.split("_");
  if (parts.length < 2) {
    return true;
  }
  const size = parts[1];
  return [PAGE_SIZE_LETTER, PAGE_SIZE_LEGAL, PAGE_SIZE_LEDGER, PAGE_SIZE_STATEMENT, PAGE_SIZE_QUARTER].includes(size);
}

